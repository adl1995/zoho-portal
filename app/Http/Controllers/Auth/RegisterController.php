    /**
     * Verify an account.
     *
     * @param
     * @return
     */
    protected function verifyAccount()
    {
        $user = User::where('verify_key', Input::get('verificationkey'))->first();
        $user->is_verified = true;
        $user->save();
        
        return redirect('home');
